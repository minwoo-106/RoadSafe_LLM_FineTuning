def make_output(summary, issues, rule, actions, caution):
    return (
        f"[상황 요약]\n{summary}\n\n"
        f"[핵심 쟁점]\n" + "\n".join(f"- {x}" for x in issues) + "\n\n"
        f"[관련 규칙]\n{rule}\n\n"
        f"[대처 방법]\n" + "\n".join(f"{i+1}. {x}" for i, x in enumerate(actions)) + "\n\n"
        f"[주의]\n{caution}"
    )

def row(input_text, summary, issues, rule, actions, caution):
    return {
        "instruction": instruction,
        "input": input_text,
        "output": make_output(summary, issues, rule, actions, caution),
    }

