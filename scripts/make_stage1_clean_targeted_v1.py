for r in mix:
    if r["input"] not in seen:
        unique.append(r)
        seen.add(r["input"])

with COMBINED_OUT.open("w", encoding="utf-8") as f:
    for r in unique:
        f.write(json.dumps(r, ensure_ascii=False) + "\n")

